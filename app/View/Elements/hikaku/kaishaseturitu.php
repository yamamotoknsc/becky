<h2 class="kz-header kz-header-point"><i class="ace-icon glyphicon glyphicon-align-left"></i>&nbsp;価格</h2>
<p>法人設立にかかる費用は法人の形態によって異なります。株式会社と合同会社では、株式会社よりも合同会社の方が費用が安いです。たとえば、株式会社を設立する手続きを自分で行うと、以下の様な手数料がかかります。<br>
・定款認証手数料(52,000円)<br>
・印紙代(40,000円)<br>
・登録免許税(150,000円)<br>
=合計242,000円<br>
また、見えない費用として軽視されがちですが、法務局や公証役場までの交通費や参考とする法人設立の書籍代などがかかることも忘れてはいけません。
印紙代である40,000円は電子定款にすれば費用はかかりませんが、電子定款を作成するには環境を整える必要がありますし、電子定款を作成し、提出するにはAdobeソフトのAcrobatやICカードリーダーを揃え、
住民基本台帳のICカードを取得しなければなりません。これらを踏まえ、法人設立代行業者に法人設立を依頼する場合、多くの場合電子定款で行うので40,000円安くなり、
代行手数料がかかったとしても自分自身で法人設立を行うより費用を抑えることができるでしょう。また、法人の実印もまとめて依頼できるなどのオプションがあるプランを提供するなど、
法人設立代行業者によりサービス・価格様々です。ビズランでは、「<strong>価格</strong>」として比較項目を設け、その評価がわかるようになっています。
</p>
<h2 class="kz-header kz-header-point"><i class="ace-icon glyphicon glyphicon-align-left"></i>&nbsp;設立後のサポート</h2>
<p>法人設立した後のアフターサービス、サポートも法人設立代行業者選びの比較ポイントになります。<br>
事業を推進していくにも雇用をするにも、仕入や人件費などのコストがかかります。法人設立代行業者のなかには、法人設立した後も会社運営に役立つ情報を提供してくれるところもあります。
事業に対しての融資を受けるために、金融公庫や商工会議所を紹介してくれたり、雇用の際には助成金を受けれるように社会保険労務士を紹介してくれるなど、
その後の会社運営のサポートまで行ってくれる法人設立代行業者も多く存在しますし、減価償却や損金など税金に関するサポートも受けられます。
ビズランでは、「<strong>設立後のサポート</strong>」の比較項目を設け、会社設立後のサポート体制の評価がわかるようになっています。
</p>

