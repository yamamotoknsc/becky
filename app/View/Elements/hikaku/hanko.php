<h2 class="kz-header kz-header-point"><i class="ace-icon glyphicon glyphicon-align-left"></i>&nbsp;品質</h2>
<p>	品揃えや印材の種類が豊富な印鑑の通販サイトでは、とてもリーズナブルな印材も取り扱っていますが、そのような低価格の印材は印影が簡単に欠けてしまうこともあります。
印影が欠けるなどして、登録した印影と異なってしまう場合は再度、登録することになり返って手間がかかり、高くついてしまう場合もあります。<br>
印材の中には、歪みやひび割れに強い玄武・耐久性に優れたカエデなど木材を利用したものや、貴重なマンモスの象牙・美しい黒が特徴的な黒水牛。そして、おしゃれなアクリル樹脂・捺印性に優れたクロム・チタンなど、実に多岐にわたる材質があります。この多くの印材を扱うハンコ制作会社のハンコの品質も様々。<br>
ハンコの通販サイトの中には、印影の確認がリアルタイムでできるところや保証期間を設けるなどして、印鑑の品質を保証しているところもあります。
これから長い付き合いを続ける法人印。「<strong>せっかく作ったのにかけてしまったので、印鑑を作成しなおさないと</strong>」なんてことがないよう、印材や品質にはこだわりたいところ。<br>
