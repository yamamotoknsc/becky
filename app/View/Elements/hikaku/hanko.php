<h2 class="kz-header kz-header-point"><i class="ace-icon glyphicon glyphicon-align-left"></i>&nbsp;納期</h2>
<p>	会社設立の登記が完了したら、法務局で代表者印の登録申請を行います。また登記後も、税務署への届け出・法人用通帳の作成など、登記完了後もさまざまな手続きや申し込みなどで、印鑑証明書と代表者印の捺印が必要な場面があります。会社設立の登記は完了したけれど「<strong>手元に代表者印がない！</strong>」「<strong>法人口座を開設できるのに銀行印がない！</strong>」とならないように、法人用の印鑑を注文する印鑑通販サイトでどの程度納期がかかるのかもしっかり確認しておきましょう。<br>
印鑑通販サイトでは、即日出荷サービスを行っているところや、土日祝も即日発送OKの業者もあります。<br>
