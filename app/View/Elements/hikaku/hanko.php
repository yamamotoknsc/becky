品揃え</h2>
<p>	法人設立をする際に必ず必要となるのが代表者印、法人実印と呼ばれる印鑑です。この代表者印(法人実印)は、法人設立の際、法務局に届け出をしなくてはならない重要な印鑑です。
この印鑑の素材（印材）には象牙・つげ・チタン・水牛・玄武・こはく・彩樺・ルビー・など実に様々な素材があり、その耐久性・質感も様々です。
せっかくの法人設立で、これからもずっと使い続けるハンコ。自分が気に入った素材・色・質感のものを使いたいですよね？
サイトによって扱っている印材の種類や金額も様々です。本当に自分が今後も使いたい印鑑は複数の印鑑サイトを比較してじっくり選びましょう。<br>
次に選ばなければいけないのが、印鑑の書体。代表者印の書体の決まりや印鑑の形態に関するルールは特にありませんが、直径のサイズ18mmの丸い印鑑が一般的です。<br>
代表者印の他にも、法人口座の開設に使用する銀行印、見積書や請求書、領収書などに押印する角印、郵送する封筒や自筆のサインの代わりとして使用するゴム印などがあります。
代表者印とは違い、必ずなければならないというものではありませんが、代表者印は法人の実印となるので、悪用されたら大問題です。<br>
そのトラブル回避のためにも、印鑑は用途に合わせて作成し、使用するのが良いでしょう。各社「<strong>法人印セット</strong>」や「<strong>会社設立セット</strong>」としてセット販売しているところもあり、単体で購入するよりも結果的に安くなる場合もあるので、印鑑通販サイトの品揃えも合わせて確認すると良いでしょう。<br>
ビズランでは、ハンコの「<strong>品揃え</strong>」の比較項目で、その評価がわかるようになっています。
