使いやすさ</h2>
<p>	ネットショップを開業するには、楽天市場やYahoo!ショッピングなどのオンラインショッピングモールと呼ばれるポータルサイトに出店する以外にも、ASPサービスを利用することで独自のネットショップを開業することができます。ASPとは、アプリケーション・サービス・プロバイダーのことで、<a href="http://bizran.jp/List/cart">ショッピングカート</a>機能などのWebアプリケーションをレンタルできるよう、サービスを提供している会社のことです。<br>
ASPを活用することにより、オンラインショッピングモールに出店するよりも、費用を抑えてネットショップを運営できたり、Webサイトのデザインをアレンジも柔軟に行うことができます。また、顧客管理に独自のシステムを連携することによって顧客サポートが柔軟にできることでもショッピングカートのASPサービスは人気です。<br>
ネットショップ開業にあたり、販売する商品やサービスの選定、商品やサービスの画像の用意、商品やサービスのPRコメントはもちろんのこと、法律で定められている「<strong>特定商取引法</strong>」の表記や支払い方法、送料の設定や返品・返金の条件など、ネットショップを開業するには多くのことを決めなければなりません。
これらの情報やデータは管理画面から入力、登録することになります。頻繁に活用し、目にする事になる管理画面は各ショッピングカートのASPサービスで異なりますので、管理画面の使い勝手やデザインはショッピングカートのASPサービスを選ぶ際の重要な比較ポイントになります。ショッピングカートのASPサービスのなかでは、管理画面のデモを用意してくれているところもあります。事前に管理画面の使用具合を確かめることをおすすめします。<br>
ビズランでは、ショッピングカートのASPサービスの管理画面の操作性を「<strong>使いやすさ</strong>」の比較項目を設け、その評価がわかるようになっています。
